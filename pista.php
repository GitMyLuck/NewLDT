	<div class="titolo-par">LA NOSTRA PISTA</div>

	<div class="testo-par">
		Situata nella bonifica di Bientina in zona Castelvecchio di Compito è una pista per ultraleggeri e deltaplani che condividono pacificamente con noi, della divisione aeromodellistica, la passione per tutto ciò che vola.. (A parte le zanzare.)<br /><br />
		<img class="alignright" src="img/est.jpg" width="300" />
		Si tratta di una splendida pista in erba lunga circa 400 metri e larga circa 30 metri.
		L'orientamento è 276° - 96°.<br /><br />
		<span class="capital">A</span><b>CCESSO AL CAMPO DI VOLO</b><br />
	L'accesso al campo di volo è riservato ai <b>Soci</b> del Gruppo, mentre eventuali ospiti vi possono accedere previa autorizzazione da richiedersi ad un componente del Consiglio Direttivo, oppure potranno presentarsi accompagnati da uno dei <b>Soci</b>.<br />Tutti i nostri <b>Soci</b> rispettano rigorosamente le indicazioni comportamentali di sicurezza indicate nel nostro regolamento.<br />
	Agli ospiti, dunque, è fatto obbligo di seguire tali indicazioni.<br />Per maggiori informazioni <br />puoi consultare il nostro regolamento scaricandolo 
	<a href="public/php/pdf/Statuto_Delta_Team.pdf">qui</a>.<br /><br />
	
	<span class="capital">M</span><b>ANTENIMENTO PISTA</b><br />Il nostro gruppo mantiene al meglio il manto erboso rasandolo periodicamente. In condizioni di meteo molto avverso (pioggia battente ed abbondante) il sistema di drenaggio della pista funziona eccellentemente (provato sulla nostra pelle sigh..).

